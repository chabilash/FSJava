package com.assignment.April_02_2024;

public class Q5_PlayWithNubersPattern {
    public static void main(String[] args) {
        int num = 4;

        for(int i=0;i<=num;i++){
            for(int j=i;j<=num;j++){
                System.out.print(num+" ");
            }
            System.out.println();

        }
    }
}
